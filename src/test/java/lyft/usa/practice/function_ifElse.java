package lyft.usa.practice;

public class function_ifElse {
	

	public static void main(String[] args) {
		
		int time = 21; 

		if (time < 12) {
		  System.out.println("Good Morning.");
		} 

		else if (time < 18) {
		  System.out.println("Good Afternoon .");
		} 

		else {
		  System.out.println("Good Evening.");
		}
	
}
	
}
